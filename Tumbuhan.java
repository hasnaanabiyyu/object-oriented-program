public class Tumbuhan extends makhlukHidup {
    private String namaTumbuhan;
    private String jaringanPengangkut;
    private String siklusHidup;
    private String alatReproduksi;

    /**
     * @return the namaTumbuhan
     */
    public String getNamaTumbuhan() {
        return namaTumbuhan;
    }

    /**
     * @param namaTumbuhan the namaTumbuhan to set
     */
    public void setNamaTumbuhan(String namaTumbuhan) {
        this.namaTumbuhan = namaTumbuhan;
    }

    /**
     * @return the jaringanPengangkut
     */
    public String getJaringanPengangkut() {
        return jaringanPengangkut;
    }

    /**
     * @param jaringanPengangkut the jaringanPengangkut to set
     */
    public void setJaringanPengangkut(String jaringanPengangkut) {
        this.jaringanPengangkut = jaringanPengangkut;
    }

    /**
     * @return the siklusHidup
     */
    public String getSiklusHidup() {
        return siklusHidup;
    }

    /**
     * @param siklusHidup the siklusHidup to set
     */
    public void setSiklusHidup(String siklusHidup) {
        this.siklusHidup = siklusHidup;
    }

    /**
     * @return the alatReproduksi
     */
    public String getAlatReproduksi() {
        return alatReproduksi;
    }

    /**
     * @param alatReproduksi the alatReproduksi to set
     */
    public void setAlatReproduksi(String alatReproduksi) {
        this.alatReproduksi = alatReproduksi;
    }
}