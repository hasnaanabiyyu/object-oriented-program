    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        try {
            final String sql = "INSERT INTO buku(ISBN, judul_buku, tahun_terbit, penerbit) VALUES (?,?,?,?)";
            final PreparedStatement ps = connection.prepareStatement(sql);
            if (txtIsbn.getText().isEmpty() || txtJudul.getText().isEmpty() || txtPenerbit.getText().isEmpty() || txtTahun.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Isi Semua Kolom terlebih dahulu");
            } else {
                ps.setString(1, txtIsbn.getText());
                ps.setString(2, txtJudul.getText());
                ps.setString(3, txtTahun.getText());
                ps.setString(4, txtPenerbit.getText());
                ps.execute();
                JOptionPane.showMessageDialog(null, "Berhasil di simpan");
            }
            this.getAllData();
            this.refreshModel();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "gagal disimpan");
            System.out.println(e.getMessage());
        }
        txtIsbn.setText("");
        txtJudul.setText("");
        txtTahun.setText("");
        txtPenerbit.setText("");
    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String isbn = txtIsbn.getText();
        String judul = txtJudul.getText();
        String tahun = txtTahun.getText();
        String penerbit = txtPenerbit.getText();
        try {
            final String sql = "Update buku set judul_buku= ?,  tahun_terbit =?, penerbit =? where ISBN= ?";
            final PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1, judul);
            pst.setString(2, tahun);
            pst.setString(3, penerbit);
            pst.setString(4, isbn);
            pst.executeUpdate();

            this.getAllData();
            this.refreshModel();

            JOptionPane.showMessageDialog(this, "Berhasil Memperbarui");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal Memperbarui");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

