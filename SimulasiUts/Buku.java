    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            String isbn = !txtIsbn.getText().trim().isEmpty() ? txtIsbn.getText().trim() : tblBuku.getValueAt(tblBuku.getSelectedRow(), 0).toString();
            String sql = "delete from buku where ISBN = ?";
            PreparedStatement ps = Connection.prepareStatement(sql);
            ps.setString(1, isbn);
            ps.execute();
            this.getAllData();
            this.refreshModel();

            JOptionPane.showMessageDialog(this, "Data behasil dihapus");
        } catch (SQLException | ArrayIndexOutOfBoundsException ex) {
            System.out.println(ex.getMessage());
        }

        txtIsbn.setText("");
        txtJudul.setText("");
        txtPenerbit.setText("");
        txtTahun.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

